#include "ft_printf.h"

void	ft_putnstr(char const *s, int n, int *len)
{
	int	i;

	if (s)
	{
		i = 0;
		while (i < n && s[i] != '\0')
		{
			ft_putchar(s[i], len);
			i++;
		}
	}
}
